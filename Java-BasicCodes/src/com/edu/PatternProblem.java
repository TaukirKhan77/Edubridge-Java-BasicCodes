package com.edu;

public class PatternProblem {
	    public static void main(String[] args) {
	        // Part 1
	        for (int i = 1; i <= 3; i++) {
	            for (int j = 1; j <= i; j++) {
	                System.out.print(i + " ");
	            }
	            System.out.println();
	        }

	        System.out.println(); // Blank line between parts

	        // Part 2
	        for (int i = 1; i <= 2; i++) {
	            for (int j = 1; j <= i; j++) {
	                System.out.print(i + " ");
	            }
	            System.out.println();
	        }

	        System.out.println(); // Blank line between parts

	        // Part 3
	        for (int i = 1; i <= 4; i++) {
	            for (int j = 1; j <= i; j++) {
	                System.out.print(i + " ");
	            }
	            System.out.println();
	        }
	    }
	}


